-- 0182_store_pay_cycle.sql
-- Adds the per-store payroll cycle (A / B / C) to public.stores and seeds it
-- from the Sonic Store Listing (HK). Surfaced as a store attribute on the My
-- Stores → store detail page and editable in the admin Edit Store modal.
--
-- Each store runs on one of the two staggered biweekly payrolls (A or B); a
-- handful of Northeast stores run a third cycle (C). Optional, nullable.
-- Idempotent. Apply via the Supabase SQL editor against Soar Hub v2.

alter table stores
  add column if not exists pay_cycle text;

alter table stores drop constraint if exists stores_pay_cycle_chk;
alter table stores
  add constraint stores_pay_cycle_chk
  check (pay_cycle is null or pay_cycle in ('A', 'B', 'C'));

