-- supabase/migrations/0135_schema_migrations.sql
--
-- Migration tracking. Records which migrations have been applied so "is X
-- live?" is answerable in a query — ending the recurring "page 400s because a
-- migration wasn't run" problem.
--
-- CONVENTION (enforced by CI, scripts/check-migrations.mjs): every NEW
-- migration's last statement records itself, e.g.
--   insert into public.schema_migrations (id) values ('0136_my_change')
--   on conflict (id) do nothing;
--
-- The backfill below marks every migration through 0134 as applied (the app
-- relies on them, so they're live). If you somehow have NOT run one of the
-- most recent ones, delete its row after applying this, then run it.

create table if not exists public.schema_migrations (
  id          text primary key,
  applied_at  timestamptz not null default now()
);

